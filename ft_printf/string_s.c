#include "ft_printf.h"

int	string_s(char *c)
{
	int	i;

	i = 0;
	if (c == NULL)
	{
		write (1, "(null)", 6);
		return (6);
	}
	while (c[i] != '\0')
	{
		character_c(c[i]);
		i++;
	}
	return (i);
}
